'use strict';


$(document).ready(function(){
    $("#menu_1").on("click", "a", function (event){
        
        
        var id = $(this).attr('href'),
            top = $(id).offset().top -240;
        $('body,html').animate({scrollTop: top}, 1500);
        
    });
    
});

